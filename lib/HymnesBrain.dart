        return _hymnesBank[hymneNumber].alto;
        break;
      case 'tenor':
        return _hymnesBank[hymneNumber].tenor;
        break;
      case 'basse':
        return _hymnesBank[hymneNumber].basse;
        break;
      default:
        return _hymnesBank[hymneNumber].basse;
    }
  }

